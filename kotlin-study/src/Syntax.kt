fun main(args: Array<String>) {

    // 1. 선언
    var i: Int
    i = 1

    var l: Long
    l = 1L

    var f: Float
    f = 1.0F

    var d: Double
    d = 1.0

    var s: String
    s = "hi"

    // 2. 초기화

    // 가변 변수 var
    var num = 1
    num = 2

    // 불변 변수 val (final 선언과 동일)
    val num2 = 1
//    num2 = 2 -> 변경 불가능

    // 선언 후 초기화는 가능
    val temp: Int
    temp = 1

}